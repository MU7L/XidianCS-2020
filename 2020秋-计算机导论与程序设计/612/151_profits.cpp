#include <stdio.h>

int main() {
	int I;
	float m;
	scanf("%d", &I);
	if (I <= 100000) {
		m = I * 0.1;
	} else if (I <= 200000) {
		m = 100000 * 0.1 + (I - 100000) * 0.075;
	} else if (I <= 400000) {
		m = 100000 * 0.1 + 100000 * 0.075 + (I - 200000) * 0.05;
	} else if (I <= 600000) {
		m = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (I - 400000) * 0.03;
	} else if (I <= 1000000) {
		m = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 400000 * 0.03 + (I - 600000) * 0.015;
	} else {
		m = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 400000 * 0.03 + 600000 * 0.015 + (I - 1000000) * 0.01;
	}
	printf("%.2f", m);
	return 0;
}
#include"stdio.h"

int main(void){
    int S,T;
    scanf("%d",&S);
    int A=S-3500;

    if(A<=0){
        T=S;
    }

    if(A>0){
        if(A<=1500){
            T=3500+A*.97;
        }
        else if(A<=4500){
            T=3500+1500*.97+(A-1500)*.9;
        }
        else if(A<=9000){
            T=3500+1500*.97+(4500-1500)*.9+(A-4500)*.8;
        }
        else if(A<=35000){
            T=3500+1500*.97+(4500-1500)*.9+(9000-4500)*.8+(A-9000)*.75;
        }
        else{
            T=3500+1500*.97+(4500-1500)*.9+(9000-4500)*.8+(35000-9000)*.75+(A-35000)*.7;
        }
    }

    printf("%d",T);
}
#include"stdio.h"
/*
标题	
查找

类别
数组
	
时间限制	
1S

内存限制	
256Kb

问题描述	
给定一个包含n个整数的数列A0,A1,A2,…An-1和一个整数k，依次输出 k在序列中出现的位置（从0开始计算）。

输入说明	
输入由两行构成，第一行为两个整数n和k，分别表示数列中整数个数和待查找整数k，n和k之间用空格分隔，0<n<100，0<k<10000。
第二行为n个整数，表示数列中的各个整数，整数之间用空格分隔，每个整数均不超过10000。

输出说明	
依次输出整数k在数列中出现的位置（从0开始计算），如果k未在数列中出现，则输出-1。

输入样例
样例1输入	
5 20
10 20 30 20 5

样例2输入
5 20
10 30 25 34 44

输出样例	
样例1输出
1 3

样例2输出
-1
	
*/

int main(void){
    int n,k,i;
    scanf("%d %d",&n,&k);
    int a[n];
    for(i=0;i<n;i++){
        scanf("%d",&a[i]);
    }
    int is=-1;
    for(i=0;i<n;i++){
        if(a[i]==k){
            printf("%d ",i);
            is=1;
        }
    }
    if(is==-1){
        printf("-1");
    }
}